
public class E01_WhileEx {
	/*
	 * 	while(조건식){
	 * 		실행할 코드
	 * 		...
	 * 		...
	 * 	}
	 * 	
	 * 	while문은 조건식이 true면 {실행할 영역} 코드를 반복한다.
	 *  조건식이 false가 되면 반복문을 멈춘다.
	 */
	public static void main(String[] args) {

		int i = 5;

		while (i > 0) {
			System.out.println("Hello World");
			i--;
		}
	}

}
