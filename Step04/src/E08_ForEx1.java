
public class E08_ForEx1 {
	/*
	 * 		for(초기화;조건식;증감식){
	 * 			실행할 코드;
	 * 			....
	 * 		}
	 */
	public static void main(String[] args) {
		int i = 0;
		
		for(i=0;i<5;i++) {
			System.out.println("Hello World");
		}
		
		System.out.println("------------");
		
		for(int j=0;j<5;j++) {
			System.out.println("Hello World");
		}
	}

}










